function getDay(number) {
  switch (number) {
    case 0:
      return "Понедельник"
      break;
    case 1:
      return "Вторник"
      break;
    case 2:
      return "Среда"
      break;
    case 3:
      return "Четверг"
      break;
    case 4:
      return "Пятница"
      break;
    case 5:
      return "Суббота"
      break;
    case 6:
      return "Воскресенье"
      break;
  }
}
export default getDay;
